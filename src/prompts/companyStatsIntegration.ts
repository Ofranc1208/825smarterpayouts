/**
 * Company Statistics keyword mapping for contextual awareness
 * Helps the AI recognize when to reference specific performance metrics
 */
export const COMPANY_STATS_KEYWORDS = {
  performance: ['stats', 'statistics', 'metrics', 'numbers', 'performance', 'results', 'data'],
  customers: ['customers', 'clients', 'people', 'served', 'happy', 'satisfied', 'rating'],
  financial: ['money', 'amount', 'value', 'payout', 'payment', 'cost', 'fee', 'price'],
  speed: ['fast', 'quick', 'speed', 'time', 'how long', 'timeline', 'processing'],
  quality: ['quality', 'success', 'approval', 'satisfaction', 'rating', 'guarantee'],
  comparison: ['better', 'best', 'compare', 'versus', 'industry', 'competitor', 'average'],
  growth: ['growth', 'increase', 'milestone', 'achievement', 'record', 'reached']
};

/**
 * Detects Company Statistics-related keywords in user message
 * @param message User's message text
 * @returns Array of detected statistics categories
 */
export function detectCompanyStatsCategories(message: string): string[] {
  const lowerMessage = message.toLowerCase();
  const categories: string[] = [];

  for (const [category, keywords] of Object.entries(COMPANY_STATS_KEYWORDS)) {
    if (keywords.some(keyword => lowerMessage.includes(keyword))) {
      categories.push(category);
    }
  }

  return categories;
}

/**
 * Generates Company Statistics-aware context hint for the AI
 * @param message User's message text
 * @returns Context hint string
 */
export function getCompanyStatsContextHint(message: string): string {
  const categories = detectCompanyStatsCategories(message);

  if (categories.length === 0) {
    return '';
  }

  return `
ðŸ’¡ COMPANY STATS CONTEXT: This question relates to ${categories.join(', ')}. Reference the Company Statistics section above for real-time performance metrics, customer satisfaction data, industry comparisons, and growth achievements.`;
}
